    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
 if (lblindice.getText().equals("-1")){
           JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun registro..");	
           return;
        }
        int esteIndice = Integer.parseInt( lblindice.getText());
        
        
        arrnombre.set(esteIndice, txtNombre.getText());
        arrcodigo.set(esteIndice, txtcodigo.getText());
        arrdescuento.set(esteIndice, txtdescuento.getText());
        arrdireccion.set(esteIndice, txtdireccion.getText());
        arremail.set(esteIndice, txtemail.getText());
        arrfpago.set(esteIndice, txtfpago.getText());
        arrnit.set(esteIndice, txtnit.getText());
        arrnombrep.set(esteIndice, txtnombrep.getText());
        arrplazo.set(esteIndice, txtplazo.getText());
        arrprecio.set(esteIndice, txtprecio.getText());
        arrtelefono.set(esteIndice, txttelefono.getText());
        
        JOptionPane.showMessageDialog(rootPane, "Registro Modificado...");       // TODO add your handling code here:
    }//GEN-LAST:event_btnmodificarActionPerformed

