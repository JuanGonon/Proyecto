    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        String vnit = txtnit.getText();
        String vnombre = txtNombre.getText();
        String vcodigo = txtcodigo.getText();
        String vdescuento = txtdescuento.getText();
        String vdireccion = txtdireccion.getText();
        String vemail = txtemail.getText();
        String vfpago = txtfpago.getText();
        String vnombrep = txtnombrep.getText();
        String vplazo = txtplazo.getText();
        String vprecio = txtprecio.getText();
        String vtelefono = txttelefono.getText();
     
        
        int opcion = JOptionPane.showConfirmDialog(null,"¿Graba este registro nuevo" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION) {//este da la opcion de salir del cancelar o seguir
            return;
        }

        arrnit.add(vnit);
        arrnombre.add(vnombre);
        arrcodigo.add(vcodigo);
        arrdescuento.add(vdescuento);
        arrdireccion.add(vdireccion);
        arremail.add(vemail);
        arrfpago.add(vfpago);
        arrnombrep.add(vnombrep);
        arrplazo.add(vplazo);
        arrprecio.add(vprecio);
        arrtelefono.add(vtelefono);
        
        lblcantreg.setText(Integer.toString(arrnit.size()));//mnda el numero de registros

        JOptionPane.showMessageDialog(null, "Registro grabado...");

        this.Limpiar();
        
        try{
           Connection con = Conexion.getConexion();
           PreparedStatement ps = con.prepareStatement("INSERT INTO Proveedores (NIT,Nombre,Codigo, Direccion, Email, Pago, NombrePro, Plazo, Precio, Telefono)VALUES (?,?,?,?,?,?,?,?,?,?)");
           ps.setString(1, vnit);
           ps.setString(2, vnombre);
           ps.setString(3, vcodigo);
           ps.setString(4, vdireccion);
           ps.setString(5, vemail);
           ps.setString(6, vfpago);
           ps.setString(7, vnombrep);
           ps.setString(8, vplazo);
           ps.setString(9, vprecio);
           ps.setString(10, vtelefono);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "Proveedor Guardado...");
           
        }catch (SQLException ex){
            
        JOptionPane.showMessageDialog(null, ex.toString());    
        }    
    }//GEN-LAST:event_btnguardarActionPerformed

