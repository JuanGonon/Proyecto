    private void btnBusca2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusca2ActionPerformed
        // TODO add your handling code here:
        int indice = arrNit.indexOf(txtNit.getText());
        if (indice != -1){
            txtNombres.setText((String)arrNombres.get(indice));
            txtEdad.setText((String) arrEdad.get(indice));
            
            String contenido =(String) arrSexo.get(indice);
            if (contenido =="Masculino"){
                cbxSexo.setSelectedIndex(0);
            }else{
                cbxSexo.setSelectedIndex(1);
            }
            txtId.setText(Integer.toString(indice));
        }else{
            txtId.setText("-1");
            JOptionPane.showMessageDialog(rootPane, "Este nit No existe (*-*)...");
        }
    }//GEN-LAST:event_btnBusca2ActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if(txtId.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun registro...");
            return;
        }
        int opcion = JOptionPane.showConfirmDialog(null, "¿Desea Elimiar este Registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION){
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        arrNit.remove(id);
        arrNombres.remove(id);
        arrEdad.remove(id);
        arrSexo.remove(id);
        txtId.setText ("-1");
        lblCantReg.setText(Integer.toString(arrNit.size()));
        JOptionPane.showMessageDialog(rootPane, "Registro Eliminado Correctamente...");
        this.Limpiar();
        
         try{
           Connection con = Conexion.getConexion();
           PreparedStatement ps = con.prepareStatement("DELETE FROM Clientes WHERE id=?");
           
           JOptionPane.showMessageDialog(null, "Registro ELIMINADO SQL...");
            ps.setInt(1, id);
        }catch (SQLException ex){
            
        JOptionPane.showMessageDialog(null, ex.toString());    
        }    
    }//GEN-LAST:event_btnEliminarActionPerformed

