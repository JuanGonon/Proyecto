    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here
          String vNit = txtNit.getText();
        String vNombres = txtNombres.getText ();
        String vEdad = txtEdad.getText();
        String vSexo = (String) cbxSexo.getSelectedItem ();
        
        
        if (txtId.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun registro...");
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        String esteSexo = (String)cbxSexo.getSelectedItem();
        
        int opcion = JOptionPane.showConfirmDialog(null, "¿Desea Modificar el Registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION){
            return;
        }
        
        arrNombres.set(id, txtNombres.getText());
        arrEdad.set(id, txtEdad.getText());
        arrSexo.set(id, esteSexo);
        JOptionPane.showMessageDialog(rootPane, "Registro Modificado Correctamente...");
        
        
        try{
           Connection con = Conexion.getConexion();
           PreparedStatement ps = con.prepareStatement("UPDATE Clientes SET Nombre=?, edad=?, sexo=? WHERE id=?");
           ps.setString(1, vNombres);
           ps.setString(2, vEdad);
           ps.setString(3, vSexo);
           ps.setInt(4, id);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "Registro Modificado SQL...");
           
        }catch (SQLException ex){
            
        JOptionPane.showMessageDialog(null, ex.toString());    
        }    
         
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        String vNit = txtNit.getText();
        String vNombres = txtNombres.getText ();
        String vEdad = txtEdad.getText();
        String vSexo = (String) cbxSexo.getSelectedItem ();
        
        int opcion = JOptionPane.showConfirmDialog(null, "¿Grabar este registro Nuevo" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION){
            return;
        }
        arrNit.add(vNit);
        arrNombres.add(vNombres);
        arrEdad.add(vEdad);
        arrSexo.add(vSexo);
        lblCantReg.setText(Integer.toString(arrNit.size()));
        
   
        JOptionPane.showMessageDialog(null, "Registro Guardado...");
        this.Limpiar();
        
        try{
           Connection con = Conexion.getConexion();
           PreparedStatement ps = con.prepareStatement("INSERT INTO Clientes (NIT,Nombre, edad, sexo)VALUES (?,?,?,?)");
           ps.setString(1, vNit);
           ps.setString(2, vNombres);
           ps.setString(3, vEdad);
           ps.setString(4, vSexo);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "Registro Guardado...");
           
        }catch (SQLException ex){
            
        JOptionPane.showMessageDialog(null, ex.toString());    
        }    
          
    }//GEN-LAST:event_btnGuardarActionPerformed

